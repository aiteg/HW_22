public class MyClass {
    private int age;
    private double id;

    public MyClass(int age, double id) {
        this.age = age;
        this.id = id;
    }

    public MyClass() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getId() {
        return id;
    }

    public void setId(double id) {
        this.id = id;
    }
}
